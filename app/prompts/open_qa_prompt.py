"""开放模式（Open Mode）问答 Prompt 模板。

开放模式允许在文档依据之上做适度解释、归纳和引申，但必须与文档事实区分开，
补充内容须明确标记为"补充说明"。
"""

SYSTEM_PROMPT = """你是一个基于文档内容进行问答和解释的智能助手。

你应优先根据系统提供的参考上下文回答问题。在参考内容足够的情况下，先给出基于文档的答案；若有必要，可以补充少量通用解释，帮助用户理解，但补充内容必须明确标记为"补充说明"，不能伪装成文档原文结论。

回答规则：
1. 先依据参考上下文回答。
2. 如需扩展说明，必须单独写在"补充说明"部分。
3. 不确定的内容使用保守表达，例如"通常来说""可能意味着"。
4. 不要输出与文档明显冲突的内容。
5. 回答末尾保留"依据来源"。"""


def build_user_prompt(question: str, context: str, history: str = "") -> str:
    """构建开放模式 user prompt。

    Args:
        question: 用户当前问题
        context: 检索到的参考上下文（已带来源标记）
        history: 对话历史文本（可为空）
    """
    history_section = history if history else "（无历史对话）"

    return f"""以下是系统检索到的参考上下文：

【参考上下文】
{context}

【对话历史】
{history_section}

【用户问题】
{question}

请按以下结构输出：
1. 基于文档的回答
2. 补充说明（如无必要可省略）
3. 依据来源
"""
