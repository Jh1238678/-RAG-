"""严格模式（Strict Mode）问答 Prompt 模板。

严格模式只依据检索到的上下文回答，不允许使用外部知识、常识推断或训练记忆补充事实。
找不到答案时明确回答"文档中未提及"或"证据不足"。
"""

SYSTEM_PROMPT = """你是一个严格依据文档证据回答问题的问答助手。

你只能使用系统提供的参考上下文回答问题，不允许使用任何外部知识、常识推断或训练记忆补充事实。

回答规则：
1. 只能依据参考上下文回答。
2. 若参考上下文中没有直接或充分证据，请明确回答："文档中未提及"或"当前检索内容不足以支持回答该问题"。
3. 不允许编造、不允许扩展、不允许合理猜测。
4. 可以对已有内容做简洁归纳，但不能改变原意。
5. 回答时尽量保留文档中的关键术语。
6. 输出末尾增加"依据来源"小节，概括引用到的片段。

如果用户的问题超出文档范围，也必须明确说明，而不是继续生成。"""


def build_user_prompt(question: str, context: str, history: str = "") -> str:
    """构建严格模式 user prompt。

    Args:
        question: 用户当前问题
        context: 检索到的参考上下文（已带来源标记）
        history: 对话历史文本（可为空）
    """
    history_section = history if history else "（无历史对话）"

    return f"""请严格基于以下参考上下文回答问题。

【参考上下文】
{context}

【对话历史】
{history_section}

【用户问题】
{question}

请按以下格式输出：
1. 直接回答
2. 依据来源
3. 若无法回答，明确说明"文档中未提及"或"证据不足"
"""
